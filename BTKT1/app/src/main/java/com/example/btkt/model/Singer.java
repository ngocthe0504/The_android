package com.example.btkt.model;

public class Singer {
    public String name;
    public int img;
    public String StageName;
    public float star;
    public String Country;

    public Singer(String name, int img, String stageName, float star, String country) {
        this.name = name;
        this.img = img;
        StageName = stageName;
        this.star = star;
        Country = country;
    }
}
